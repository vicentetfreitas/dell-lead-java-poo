/*
 * Estrutura for:
 * 
 * for(inicialização; expressão booleana; passo da repetição){
 *  instruções
 * }
 * 
 * inicialização: inicialização das variáveis
 * condição: condicionais como nas repetições while e do-while
 * passo da repetição:manipulação de variáveis relacionadas à condição de 
 * repetição do for envolvendo geralmente encremento e decremendo
 *
 * 
 * */
public class UsaFor {
	public static void main(String[] args) {
		for(int  i=1 ; i< 5;i++) {
			System.out.println("O valor da variável i = " +1);
		}
	}

}
